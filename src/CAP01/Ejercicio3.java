/* Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su
correspondiente traducción al castellano. Las palabras deben estar distribuidas
en dos columnas y alineadas a la izquierda. */
public class Ejercicio3 {
    public static void main(String[] args) {
        System.out.printf("%-10s %-10s\n", "computer", "ordenador");
        System.out.printf("%-10s %-10s\n", "come", "ven");
        System.out.printf("%-10s %-10s\n", "table", "mesa");
        System.out.printf("%-10s %-10s\n", "chair", "silla");
        System.out.printf("%-10s %-10s\n", "fan", "ventilador");
        System.out.printf("%-10s %-10s\n", "screen", "pantalla");
        System.out.printf("%-10s %-10s\n", "bottle", "botella");
        System.out.printf("%-10s %-10s\n", "water", "agua");
        System.out.printf("%-10s %-10s\n", "speaker", "altavoz");
        System.out.printf("%-10s %-10s\n", "box", "caja");
    }
}